-- Migration 001: Create johnb (John B) user for Digital Response tenant
--                and ensure Peter Bardenhagen (TL0025) has Admin access to ALL tenants.
-- Runs automatically at next startup via MigrationRunnerService; deletes itself on success.

-- ── 1. Create johnb user if not already present ─────────────────────────────
-- BCrypt hash of 'bardie123' (workFactor 12)
